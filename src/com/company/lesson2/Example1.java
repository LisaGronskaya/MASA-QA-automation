package com.company.lesson2;

public class Example1 {
    public static void main(String[] args) {

        byte myByte1 = 1;
        byte myByte2 = 2;
        short myShort1 = 3;
        short myShort2 = 4;
        int myInt1 = 5;
        int myInt2 = 6;
        long myLong1 = 7L;
        long myLong2 = 8L;
        System.out.println(myByte1 + " " + myByte2 + " " + myShort1 + " " + myShort2 + " " + myInt1 + " " + myInt2 + " " + myLong1 + " " + myLong2);

        float myFloat1 = 9f;
        float myFloat2 = 10f;
        double myDouble1 = 11;
        double myDouble2 = 12;
        boolean myBoolean1 = true;
        boolean myBoolean2 = false;
        System.out.println(myFloat1 + " " + myFloat2 + " " + myDouble1 + " " + myDouble2 + " " + myBoolean1 + " " + myBoolean2);

        char myChar1 = 'l';
        char myChar2 = 'o';
        char myChar3 = 'l';
        System.out.println(myChar1 + ""  + myChar2 + "" + myChar3);
    }
}

