package com.company.lesson3_1;

public class IfElse {
    public static void main(String[] args) {

        int a = 15;

        if (a >= 5) {
            System.out.println("a >= 10");
        } else if (a == 0) {
            System.out.println("a == 0");
        } else {
            System.out.println("a < 10");
        }
    }
}
